		if (!isset($GLOBALS["CACHE_PARTY"])) {
			$GLOBALS["CACHE_PARTY"]=DataGetCurrentPartyConf();
		} 

		// Store info.
		$db->insert(
			'eventlog',
			array(
				'ts' => $gameRequest[1],
				'gamets' => $gameRequest[2],
				'type' => 'chat',
				'data' => "The Narrator. {$GLOBALS["HERIKA_NAME"]} reads in quest journal:".prettyPrintJson($returnFunction[3]),
